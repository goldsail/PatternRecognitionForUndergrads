function w_update = update_weights(X, y, j, a, d, w, c)
% 更新权值
% 
% 输入
%     X        : n * p 矩阵，每一行是一个训练样本
%     y        : n * 1 向量，每一行是一个训练标签
%     j        : 所选择的特征维度
%     a        : 所选阈值
%     d        : 1 或 -1
%     w        : n * 1 向量, 原来的权值
%     c        : 各个分类器的权值
%
% Output
%     w_update : n * 1 向量, 更新过后的权值

%%% 请补全代码 %%%


p = ((X(:, j) <= a) - 0.5) * 2 * d; % prediction labels
w_update = w .* exp(c * (p ~= y)); % update the weights

w_update = w_update / sum(w_update); % normalization


%%% 请补全代码 %%%
end